<?php
	function MUTA($s){
		$ea = ['/[áàảãạăắằẳẵặâấầẩẫậ]/','/[đ]/','/[éèẻẽẹêếềểễệ]/','/[íìỉĩị]/','/[óòỏõọôốồổỗộơớờởỡợ]/','/[úùủũụưứừửữự]/','/[ýỳỷỹỵ]/'];
		$ra = ['a','d','e','i','o','u','y'];
		$r = mb_strtolower($s);
		$i = 0;
		foreach($ea as $v){
			$r = mb_ereg_replace($v, $ra[$i], $r);
			$i++;
		}
		return $r;
	}

?>