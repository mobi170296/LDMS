<?php
	function MUTA($s){
		$ea = ['/á|à|ả|ã|ạ|ă|ắ|ằ|ẳ|ẵ|ặ|â|ấ|ầ|ẩ|ẫ|ậ/','/đ/','/é|è|ẻ|ẽ|ẹ|ê|ế|ề|ể|ễ|ệ/','/í|ì|ỉ|ĩ|ị/','/ó|ò|ỏ|õ|ọ|ô|ố|ồ|ổ|ỗ|ộ|ơ|ớ|ờ|ở|ỡ|ợ/','/ú|ù|ủ|ũ|ụ|ư|ứ|ừ|ử|ữ|ự/','/ý|ỳ|ỷ|ỹ|ỵ/'];
		$ra = ['a','d','e','i','o','u','y'];
		$r = mb_strtolower($s);
		$i = 0;
		foreach($ea as $v){
			$r = preg_replace($v, $ra[$i], $r);
			$i++;
		}
		return $r;
	}

?>